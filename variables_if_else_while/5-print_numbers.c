#include <stdio.h>

/**
  * main - Entry point of program.
  * takes no argument.
  * Description: prints all single digit numbers of base 10 starting from 0, followed by a new line.
  *
  * return: Returns (0) always on success.
  */

int main(void)
{
	char i;

	for (i = 48; i <= 57; i++)
		putchar(i);

		putchar(10);
}
